package com.dexels.data;

public class KeyPress {
	int keypress_timestamp;
	int timestamp;
	
	public KeyPress(){
	}
	
	public int getKeypressTimestamp() {
		return keypress_timestamp;
	}

	public void setKeypressTimestamp(int keypress_timestamp) {
		this.keypress_timestamp = keypress_timestamp;
	}

	public int getTimestamp() {
		return timestamp;
	}

	public void setTimestamp(int timestamp) {
		this.timestamp = timestamp;
	}

	
	
}
